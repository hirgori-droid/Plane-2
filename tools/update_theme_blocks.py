def strip_accents(text: str) -> str:
    return "".join(
        ch for ch in unicodedata.normalize("NFD", text)
        if unicodedata.category(ch) != "Mn"
    )


def transliterate(text: str) -> str:
    plain = strip_accents(text.lower())
    result: list[str] = []
    for char in plain:
        result.append(CYRILLIC_TO_LATIN.get(char, char))
    return f"/{''.join(result)}/"


def safe_accent_word(word: str, accentor) -> str:
    accented = accentor.put_accent(word).replace("'", ACCENT_CHAR).strip()
    if strip_accents(accented).lower() == word.lower():
        return accented
    return word


def safe_accent_text(text: str, accentor) -> str:
    if text in MANUAL_ACCENTS:
        return MANUAL_ACCENTS[text]

    def replace(match: re.Match[str]) -> str:
        token = match.group(0)
        if token in MANUAL_ACCENTS:
            return MANUAL_ACCENTS[token]
        return safe_accent_word(token, accentor)

    return WORD_TOKEN_PATTERN.sub(replace, text)


def build_examples(word: str) -> str:
    return f'Это "{word}". | Я учу слово "{word}".'


def build_rows(block_id: str, terms: list[tuple[str, str]], accentor) -> list[list[str]]:
    rows: list[list[str]] = []
    for russian, english in terms:
        accented = safe_accent_text(russian, accentor)
        rows.append(
            [
                block_id,
                accented,
                transliterate(accented),
                english,
                english,
                build_examples(accented),
            ]
        )
    return rows


