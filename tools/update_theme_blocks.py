def load_existing_rows() -> list[list[str]]:
    with THEME_BLOCKS_PATH.open("r", encoding="utf-8", newline="") as handle:
        reader = csv.reader(handle, delimiter=";")
        header = next(reader)
        if header != HEADER:
            raise SystemExit(f"Unexpected header in {THEME_BLOCKS_PATH.name}: {header!r}")
        return [row for row in reader]


def main() -> int:
    accentor = load_accentor()
    existing_rows = load_existing_rows()
    replaced_blocks = set(BLOCKS.keys())
    preserved = [row for row in existing_rows if row[0] not in replaced_blocks]

    generated: list[list[str]] = []
    for block_id, terms in BLOCKS.items():
        if len(terms) != len({russian for russian, _english in terms}):
            raise SystemExit(f"Duplicate Russian headwords in {block_id}")
        generated.extend(build_rows(block_id, terms, accentor))

    output_rows = preserved + generated
    with THEME_BLOCKS_PATH.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.writer(handle, delimiter=";", lineterminator="\n")
        writer.writerow(HEADER)
        writer.writerows(output_rows)

    for block_id, terms in BLOCKS.items():
        print(f"{block_id}: {len(terms)} rows")
    print(f"Wrote {len(output_rows)} theme rows to {THEME_BLOCKS_PATH}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
